    public class MathExpressions {
   
       public static void main(String[] args)   {
         double sqrt, pow;
      
         sqrt = Math.sqrt(9.0);
         System.out.println("Math.sqrt(9.0) = " + sqrt);
         pow = Math.pow(2.0,4.0);
         System.out.println("Math.pow(2.0,4.0) = " + pow);
      
      }
   } 
