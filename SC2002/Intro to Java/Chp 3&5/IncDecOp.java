public class IncDecOp {			
  public static void main(String[] args){
	int num1 = 5, num2 = 5;

	System.out.println("num1 = " + num1); 		/* first */
	System.out.println("num1++ = " + num1++);	/* second */
	System.out.println("num1 = " + num1);		/* third */
	System.out.println("++num1 = " + ++num1); 	/* fourth */
	System.out.println("num1 = " + num1);   	/* fifth */

	System.out.println("num2 = " + num2);
	System.out.println("num2-- = " + num2--);
	System.out.println("num2 = " + num2);
	System.out.println("--num2 = " + --num2);
	System.out.println("num2 = " + num2);
  }
}

